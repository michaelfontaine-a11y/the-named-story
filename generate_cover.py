if __name__ == "__main__":
    ap = argparse.ArgumentParser(description="Generate a Named Story cover wrap for Gelato hardcover")
    ap.add_argument("--name", required=True, help="Child's first name")
    ap.add_argument("--variant", required=True, help="Character variant (e.g. 'boy-fair-blonde')")
    ap.add_argument("--output", default=None, help="Output PDF filename")
    ap.add_argument("--pages", type=int, default=30, help="Interior page count (affects spine width)")
    ap.add_argument("--gelato-key", default=None, help="Gelato API key for fetching exact dimensions")
    ap.add_argument("--product-uid", default=None, help="Gelato product UID")
    a = ap.parse_args()

    if not a.output:
        a.output = f"{a.name.lower().replace(' ', '-')}-cover-wrap.pdf"

    generate_cover(a.name, a.variant, a.output, a.gelato_key, a.product_uid, a.pages)
